var findBySql=function(sql,args,callback){
    console.log("开始查询："+sql);
    var connection=db().getConnection(function(err,connection){
        if(err){
            console.log("获得connection出现错误:"+err);
            throw err;
        }
        connection.query(sql,args,function(err, rows, fields){
            if (err){
                console.log(err);
                rows=[];
            }
            callback(rows);
            connection.release();
        });
    });
};
